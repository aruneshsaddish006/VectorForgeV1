def _render_run_context_section(ctx: dict[str, Any], manifest: Any) -> str:
    parts: list[str] = ["## Run Context\n"]
    parts += _render_problem_section(ctx.get("user_request", {}))
    parts += _render_dataset_section(ctx.get("dataset_profile", {}))
    parts += _render_metric_section(ctx.get("metric_decision", {}))
    parts += _render_experiments_section(ctx.get("all_experiments", []), manifest)
    parts += _render_winner_section(ctx.get("winner", {}), ctx.get("final_recommendation", {}))
    return "\n".join(parts)


def _render_problem_section(req: dict[str, Any]) -> list[str]:
    if not req:
        return []
    problem = (
        req.get("problem_statement")
        or req.get("description")
        or req.get("document_description")
        or req.get("optimization_goal")
    )
    parts: list[str] = []
    if problem:
        parts = ["### Problem Statement\n", f"{problem}\n"]

    optimization_goal = req.get("optimization_goal")
    if optimization_goal and optimization_goal != problem:
        if not parts:
            parts = ["### Problem Statement\n"]
        parts.append(f"\n**Optimization goal:** {optimization_goal}\n")

    kpis = req.get("business_kpis") or req.get("business_kpi")
    if kpis:
        if isinstance(kpis, list):
            kpi_text = "; ".join(str(kpi) for kpi in kpis)
        else:
            kpi_text = str(kpis)
        if not parts:
            parts = ["### Problem Statement\n"]
        parts.append(f"\n**Business KPI:** {kpi_text}\n")

    if req.get("target_column"):
        if not parts:
            parts = ["### Problem Statement\n"]
        parts.append(f"\n**Target column:** `{req['target_column']}`\n")
    if req.get("rounds") or req.get("architectures_per_round"):
        if not parts:
            parts = ["### Problem Statement\n"]
        rounds = req.get("rounds", "N/A")
        architectures = req.get("architectures_per_round", "N/A")
        parts.append(f"\n**Search budget:** {rounds} rounds, {architectures} architectures per round\n")
    return parts


def _render_dataset_section(profile: dict[str, Any]) -> list[str]:
    if not profile:
        return []
    ds = profile.get("dataset", {})
    feat = profile.get("features", {})
    tgt = profile.get("target", {})
    parts: list[str] = ["\n### Dataset Profile\n"]
    detail_count = 0
    if ds:
        parts.append(f"- Rows: {ds.get('rows', 'N/A')} | Columns: {ds.get('columns', 'N/A')}")
        parts.append(f"- Missing cells ratio: {ds.get('missing_cells_ratio', 0):.1%}\n")
        detail_count += 1
    if feat:
        parts.append(f"- Numeric features: {feat.get('numeric_count', 'N/A')}")
        parts.append(f"- Categorical features: {feat.get('categorical_count', 'N/A')}\n")
        detail_count += 1
    if tgt:
        parts.append(f"- Target `{tgt.get('name', 'target')}` distribution: {tgt.get('class_distribution', {})}\n")
        detail_count += 1

    if profile.get("file_count") is not None:
        parts.append(f"- Documents: {profile.get('file_count', 'N/A')}")
        detail_count += 1
    if profile.get("extensions"):
        ext_summary = ", ".join(f"{ext}: {count}" for ext, count in profile["extensions"].items())
        parts.append(f"- File types: {ext_summary}")
        detail_count += 1
    if profile.get("human_document_description"):
        parts.append(f"- Description: {profile['human_document_description']}")
        detail_count += 1

    available_parse = [
        item.get("method")
        for item in profile.get("no_api_parse_catalog", [])
        if item.get("available") and item.get("method")
    ][:4]
    if available_parse:
        parts.append(f"- Available parsers: {', '.join(available_parse)}")
        detail_count += 1

    available_chunkers = [
        item.get("method")
        for item in profile.get("no_api_chunk_catalog", [])
        if item.get("available") and item.get("method")
    ][:4]
    if available_chunkers:
        parts.append(f"- Available chunkers: {', '.join(available_chunkers)}\n")
        detail_count += 1

    return parts if detail_count else []


def _render_metric_section(metric_dec: dict[str, Any]) -> list[str]:
    if not metric_dec:
        return []
    parts = ["\n### Metric Selection Rationale\n"]
    parts.append(f"{metric_dec.get('reasoning', '')}\n")
    parts.append(f"\n**Primary metric:** `{metric_dec.get('selected_primary_metric', 'N/A')}`")
    secondaries = metric_dec.get("secondary_metrics", [])
    if secondaries:
        parts.append(f"  |  **Secondary:** {', '.join(f'`{m}`' for m in secondaries)}\n")
    return parts


def _render_experiments_section(experiments: list[dict[str, Any]], manifest: Any) -> list[str]:
    if not experiments:
        return []
    metric_names = _experiment_metric_columns(experiments, manifest)
    parts = ["\n### Experiment History\n"]
    parts.append("| Experiment | " + " | ".join(_metric_label(name) for name in metric_names) + " |")
    parts.append("|---|" + "|".join("---" for _ in metric_names) + "|")
    for exp in experiments:
        eid = exp.get("_experiment_path", "?").split("/")[-1]
        values = [_format_metric_value(_metric_value(exp, name)) for name in metric_names]
        parts.append(f"| `{eid}` | " + " | ".join(values) + " |")
    parts.append("")
    return parts


def _render_winner_section(winner: dict[str, Any], rec: dict[str, Any]) -> list[str]:
    if not winner and not rec:
        return []
    exp_id = (
        winner.get("experiment_id")
        or winner.get("best_experiment_id")
        or rec.get("best_experiment_id", "N/A")
    )
    why = winner.get("why_selected") or rec.get("why_selected", "Highest primary metric across all completed experiments.")
    return ["\n### Winning Experiment\n", f"**`{exp_id}`** — {why}\n"]


def _experiment_metric_columns(experiments: list[dict[str, Any]], manifest: Any) -> list[str]:
    metric_names: list[str] = []

    def add(name: Any) -> None:
        if name and name != "unknown" and name not in metric_names:
            metric_names.append(str(name))

    engine_type = getattr(manifest, "engine_type", "")
    primary_metric = getattr(manifest, "primary_metric", None)
    secondary_metrics = getattr(manifest, "secondary_metrics", {}) or {}

    for name in _canonical_metric_order(engine_type):
        if name == primary_metric or name in secondary_metrics or _experiments_include_metric(experiments, name):
            add(name)

    add(primary_metric)
    for name in secondary_metrics.keys():
        add(name)

    if not metric_names:
        for exp in experiments:
            add(exp.get("primary_metric"))
            for source in (exp.get("evaluation", {}), exp.get("secondary_metrics", {})):
                for name in source.keys():
                    add(name)
            if metric_names:
                break

    return metric_names or ["primary_metric_value"]


def _ordered_secondary_metrics(
    engine_type: str,
    primary_metric: str,
    secondary_metrics: dict[str, Any],
) -> list[tuple[str, Any]]:
    ordered: list[tuple[str, Any]] = []
    seen: set[str] = set()
    for name in _canonical_metric_order(engine_type):
        if name != primary_metric and name in secondary_metrics:
            ordered.append((name, secondary_metrics[name]))
            seen.add(name)
    for name, value in secondary_metrics.items():
        if name not in seen:
            ordered.append((name, value))
    return ordered


def _canonical_metric_order(engine_type: str) -> list[str]:
    if engine_type == "autorag":
        return ["retrieval_f1", "retrieval_recall", "retrieval_precision", "rouge", "g_eval"]
    if engine_type == "autogluon_tabular":
        return ["roc_auc", "accuracy", "balanced_accuracy", "mcc", "f1", "precision", "recall"]
    return []


def _experiments_include_metric(experiments: list[dict[str, Any]], metric_name: str) -> bool:
    return any(_metric_value(exp, metric_name) is not None for exp in experiments)


def _metric_value(exp: dict[str, Any], metric_name: str) -> Any:
    if metric_name == exp.get("primary_metric") and "primary_metric_value" in exp:
        return exp.get("primary_metric_value")
    for source in (exp.get("evaluation", {}), exp.get("secondary_metrics", {}), exp):
        if metric_name in source:
            return source[metric_name]
    if metric_name == "primary_metric_value":
        return exp.get("primary_metric_value")
    return None


def _metric_label(metric_name: str) -> str:
    labels = {
        "accuracy": "Accuracy",
        "balanced_accuracy": "Balanced Accuracy",
        "f1": "F1",
        "g_eval": "G-Eval",
        "mcc": "MCC",
        "precision": "Precision",
        "recall": "Recall",
        "retrieval_f1": "Retrieval F1",
        "retrieval_precision": "Retrieval Precision",
        "retrieval_recall": "Retrieval Recall",
        "roc_auc": "ROC AUC",
        "rouge": "ROUGE",
    }
    return labels.get(metric_name, metric_name.replace("_", " ").title())


def _format_metric_value(value: Any) -> str:
    if value is None:
        return "N/A"
    if isinstance(value, (int, float)):
        return f"{value:.4f}"
    return str(value)


