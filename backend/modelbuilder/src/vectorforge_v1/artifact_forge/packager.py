def assemble(out_dir: Path, files: dict[str, str | bytes | Path]) -> None:
    out_dir.mkdir(parents=True, exist_ok=True)
    for rel_path, content in files.items():
        dest = out_dir / rel_path
        dest.parent.mkdir(parents=True, exist_ok=True)
        if isinstance(content, Path):
            if content.is_dir():
                shutil.copytree(str(content), str(dest), dirs_exist_ok=True)
            else:
                shutil.copy2(str(content), str(dest))
        elif isinstance(content, bytes):
            dest.write_bytes(content)
        else:
            dest.write_text(content, encoding="utf-8")


def pin_requirements(engine_families: list[str]) -> str:
    base_pkgs = ["autogluon-tabular"]
    extra_pkgs = []
    for family in engine_families:
        pkg = _FAMILY_PKG_MAP.get(family.upper())
        if pkg:
            extra_pkgs.append(pkg)

    lines: list[str] = []
    for pkg in base_pkgs + extra_pkgs:
        try:
            version = importlib.metadata.version(pkg)
            lines.append(f"{pkg}=={version}")
        except importlib.metadata.PackageNotFoundError:
            lines.append(f"# {pkg} not installed in current env")

    return "\n".join(lines) + "\n"


